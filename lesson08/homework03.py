class TypeErrorList(BaseException):
    pass
        

integer_list = []
stop = False

def append_number(data):
    if data.isdigit():
        integer_list.append(data)
    else:
        raise TypeErrorList(data)
    

while not stop:
    data = input("To end enter \"stop\"\nEnter the integers >>> ").split(" ")
    
    for i in data:
        try:
            append_number(i)
        except TypeErrorList:
            if i == "stop":
                stop = True
                break
            continue
            
print(integer_list)
        
                
    
    